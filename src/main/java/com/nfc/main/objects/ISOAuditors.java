package com.nfc.main.objects;

public class ISOAuditors {

	private String auditorName;
	private Integer auditorEcNo;
	private String plantName;
	private Integer plantCode;
	private String role;
	private String forUnitDescription;

	public String getAuditorName() {
		return auditorName;
	}

	public void setAuditorName(String auditorName) {
		this.auditorName = auditorName;
	}

	public Integer getAuditorEcNo() {
		return auditorEcNo;
	}

	public void setAuditorEcNo(Integer auditroEcNo) {
		this.auditorEcNo = auditroEcNo;
	}

	public String getPlantName() {
		return plantName;
	}

	public void setPlantName(String plantName) {
		this.plantName = plantName;
	}

	public Integer getPlantCode() {
		return plantCode;
	}

	public void setPlantCode(Integer plantCode) {
		this.plantCode = plantCode;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getForUnitDescription() {
		return forUnitDescription;
	}

	public void setForUnitDescription(String forUnitDescription) {
		this.forUnitDescription = forUnitDescription;
	}

}
